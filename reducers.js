import { BUY_CAKE } from "./actions.js";
import { initialState } from "./initialState.js";

export const reducer = (state = initialState, action) => {
  switch (action.type) {
    case BUY_CAKE:
      return {
        ...state,
        numOfCakes: state.numOfCakes - 1,
      };

    default:
      return state;
  }
};
